package com.okwei.supplyportal.bean.vo;

public class SupplyerMsg {
   private String image;
   private String weiname;
   private String buscontent;
   private int isyun;
   private int isbatch;
   private int isport;
public String getImage() {
	return image;
}
public void setImage(String image) {
	this.image = image;
}
public String getWeiname() {
	return weiname;
}
public void setWeiname(String weiname) {
	this.weiname = weiname;
}
public String getBuscontent() {
	return buscontent;
}
public void setBuscontent(String buscontent) {
	this.buscontent = buscontent;
}
public int getIsyun() {
	return isyun;
}
public void setIsyun(int isyun) {
	this.isyun = isyun;
}
public int getIsbatch() {
	return isbatch;
}
public void setIsbatch(int isbatch) {
	this.isbatch = isbatch;
}
public int getIsport() {
	return isport;
}
public void setIsport(int isport) {
	this.isport = isport;
}
   
}
