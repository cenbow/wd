package com.okwei.cartportal.bean;

import java.io.Serializable;
/**
 * 商品信息
 * @author yangjunjun
 *
 */
public class ProductModel implements Serializable {
	private static final long serialVersionUID = 1L;
	private Long scId;// 主键
	private Long prodId;//商品id
	private Long styleId;//商品款式id
	private Long shopWeiId;//来源微店id
	private String shopWeiName;//来源微店名称
	private String img;//商品图片
	private String prodTitle;//商品名称
	private int prodCount;//商品数量
	private Double prodNowprice;//商品单价 
	private Double totalPrice;//商品总价
	private String property;//选择商品款式名称
	private Long shelvesId;//供应商上架ID
	
	public Long getShelvesId() {
		return shelvesId;
	}
	public void setShelvesId(Long shelvesId) {
		this.shelvesId = shelvesId;
	}
	public Double getTotalPrice() {
		return totalPrice;
	}
	public void setTotalPrice(Double totalPrice) {
		this.totalPrice = totalPrice;
	}
	public Long getProdId() {
		return prodId;
	}
	public void setProdId(Long prodId) {
		this.prodId = prodId;
	}
	public Long getStyleId() {
		return styleId;
	}
	public void setStyleId(Long styleId) {
		this.styleId = styleId;
	}
	public String getShopWeiName() {
		return shopWeiName;
	}
	public void setShopWeiName(String shopWeiName) {
		this.shopWeiName = shopWeiName;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getProdTitle() {
		return prodTitle;
	}
	public void setProdTitle(String prodTitle) {
		this.prodTitle = prodTitle;
	}
	public int getProdCount() {
		return prodCount;
	}
	public void setProdCount(int prodCount) {
		this.prodCount = prodCount;
	}
	public Double getProdNowprice() {
		return prodNowprice;
	}
	public void setProdNowprice(Double prodNowprice) {
		this.prodNowprice = prodNowprice;
	}
	public String getProperty() {
		return property;
	}
	public void setProperty(String property) {
		this.property = property;
	}
	public Long getShopWeiId() {
		return shopWeiId;
	}
	public void setShopWeiId(Long shopWeiId) {
		this.shopWeiId = shopWeiId;
	}
	public Long getScId() {
		return scId;
	}
	public void setScId(Long scId) {
		this.scId = scId;
	}
}
