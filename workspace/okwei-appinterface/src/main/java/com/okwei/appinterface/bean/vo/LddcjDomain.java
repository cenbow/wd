package com.okwei.appinterface.bean.vo;

public class LddcjDomain {
	
	private Long groundId ;     //落地店编号
	private String groundName ; //落地店名称
	private String groundArea ; //落地店地区
	private int orderTotal ;    //订单总数
	
	public LddcjDomain(){
		
	}

	public Long getGroundId() {
		return groundId;
	}

	public void setGroundId(Long groundId) {
		this.groundId = groundId;
	}

	public String getGroundName() {
		return groundName;
	}

	public void setGroundName(String groundName) {
		this.groundName = groundName;
	}

	public String getGroundArea() {
		return groundArea;
	}

	public void setGroundArea(String groundArea) {
		this.groundArea = groundArea;
	}

	public int getOrderTotal() {
		return orderTotal;
	}

	public void setOrderTotal(int orderTotal) {
		this.orderTotal = orderTotal;
	}
	

}
