package com.okwei.appinterface.bean.vo.share;

import java.io.Serializable;
import java.util.List;

import com.okwei.bean.vo.share.ProductsVO;

/**
 * @author fh
 *	分享详情的商品对象
 */
public class ProductVO implements Serializable {
	private static final long serialVersionUID = 1L;
	private Long sellerWid; //上架人微店号，未上架商品可为空
	private Long productId;//商品Id
    private String productName;//商品名称
    private String productPicture;//商品主图
    private Double retailPrice;//零售价
    private Double displayPrice;//原价，可为空
    private int stockCount;//库存数量
    private int saleCount;//销量
    
    private Double commission;//佣金
    private Long shelveId;//产品上架Id
    private Long spid;
    
    private String shareDescription;//描述
    private List<ProductsVO> productPictureList;//如果是单品精选  才赋值
    
	 
	public List<ProductsVO> getProductPictureList() {
		return productPictureList;
	}
	public void setProductPictureList(List<ProductsVO> productPictureList) {
		this.productPictureList = productPictureList;
	}
	 
	public String getShareDescription() {
		return shareDescription;
	}
	public void setShareDescription(String shareDescription) {
		this.shareDescription = shareDescription;
	}
	public Long getSpid() {
		return spid;
	}
	public void setSpid(Long spid) {
		this.spid = spid;
	}
	public Double getCommission() {
		return commission;
	}
	public void setCommission(Double commission) {
		this.commission = commission;
	}
	public Long getShelveId() {
		return shelveId;
	}
	public void setShelveId(Long shelveId) {
		this.shelveId = shelveId;
	}
	public Long getSellerWid() {
		return sellerWid;
	}
	public void setSellerWid(Long sellerWid) {
		this.sellerWid = sellerWid;
	}
	public Long getProductId() {
		return productId;
	}
	public void setProductId(Long productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductPicture() {
		return productPicture;
	}
	public void setProductPicture(String productPicture) {
		this.productPicture = productPicture;
	}
	public Double getRetailPrice() {
		return retailPrice;
	}
	public void setRetailPrice(Double retailPrice) {
		this.retailPrice = retailPrice;
	}
	public Double getDisplayPrice() {
		return displayPrice;
	}
	public void setDisplayPrice(Double displayPrice) {
		this.displayPrice = displayPrice;
	}
	public int getStockCount() {
		return stockCount;
	}
	public void setStockCount(int stockCount) {
		this.stockCount = stockCount;
	}
	public int getSaleCount() {
		return saleCount;
	}
	public void setSaleCount(int saleCount) {
		this.saleCount = saleCount;
	}
     
    
	    
	    
}
