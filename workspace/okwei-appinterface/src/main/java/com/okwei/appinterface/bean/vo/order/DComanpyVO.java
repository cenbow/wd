package com.okwei.appinterface.bean.vo.order;

/**
 * @author fuhao 
 *	获取发货物流信息
 */
public class DComanpyVO {
	
	/**
	 * 供应商订单号
	 */
	private String supplierOrderId;
	/**
	 * 物流公司（名称）拼音
	 */
	private String DComanpyNo;
	/**
	 * 物流公司名称
	 */
	private String DeliveryCompany;
	/**
	 * 物流单号
	 */
	private String DeliveryOrder;
	
	/**
	 * 退款订单Id
	 */
	private Long backOrder;
	
	
	public Long getBackOrder() {
		return backOrder;
	}
	public void setBackOrder(Long backOrder) {
		this.backOrder = backOrder;
	}
	public String getSupplierOrderId() {
		return supplierOrderId;
	}
	public void setSupplierOrderId(String supplierOrderId) {
		this.supplierOrderId = supplierOrderId;
	}
	public String getDComanpyNo() {
		return DComanpyNo;
	}
	public void setDComanpyNo(String dComanpyNo) {
		DComanpyNo = dComanpyNo;
	}
	public String getDeliveryCompany() {
		return DeliveryCompany;
	}
	public void setDeliveryCompany(String deliveryCompany) {
		DeliveryCompany = deliveryCompany;
	}
	public String getDeliveryOrder() {
		return DeliveryOrder;
	}
	public void setDeliveryOrder(String deliveryOrder) {
		DeliveryOrder = deliveryOrder;
	}
	
	
	
}
