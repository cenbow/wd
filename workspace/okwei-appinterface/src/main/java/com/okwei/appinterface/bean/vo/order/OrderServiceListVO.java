package com.okwei.appinterface.bean.vo.order;

/**
 * @author fuhao
 * 订单查询参数
 */
public class OrderServiceListVO {
	/**
	 * 供应商订单号
	 */
	private String supplyOrderid;
	
	private Long weiId;
	/**
	 * 身份标示
	 */
	private String tiket;
	
	/**
	 * 1 买家 2 卖家
	 */
	private int userType;
	/**
	 * //0未付款 1.已付款 2.已发货 3 已收货 4 已完成 5 退款中 6 已退款 7 已取消 （往后就是预订单） 
	 * 8 等待确定 9 申请取消 10 已确定 11 已拒绝 12 已支付定金 13 已过期  ， （-1表示查询订单的所有状态）
	 */
	private short state;
	
	private int pageindex;
	 
	private int pagesize;
	 
	/**
	 * 1表示买家，2表示卖家
	 */
	private String userState;
	 
	/**
	 * 订单类型（1-零售；9-铺货；8-批发；7-进货）
	 */
	private short type;
  
	public Long getWeiId() {
		return weiId;
	}

	public void setWeiId(Long weiId) {
		this.weiId = weiId;
	}

	public String getSupplyOrderid() {
		return supplyOrderid;
	}

	public void setSupplyOrderid(String supplyOrderid) {
		this.supplyOrderid = supplyOrderid;
	}

	public String getTiket() {
		return tiket;
	}

	public void setTiket(String tiket) {
		this.tiket = tiket;
	}

	public int getUserType() {
		return userType;
	}

	public void setUserType(int userType) {
		this.userType = userType;
	}

	public short getState() {
		return state;
	}

	public void setState(short state) {
		this.state = state;
	}

	public int getPageindex() {
		return pageindex;
	}

	public void setPageindex(int pageindex) {
		this.pageindex = pageindex;
	}

	public int getPagesize() {
		return pagesize;
	}

	public void setPagesize(int pagesize) {
		this.pagesize = pagesize;
	}

	public String getUserState() {
		return userState;
	}

	public void setUserState(String userState) {
		this.userState = userState;
	}

	public short getType() {
		return type;
	}

	public void setType(short type) {
		this.type = type;
	}
 

}
