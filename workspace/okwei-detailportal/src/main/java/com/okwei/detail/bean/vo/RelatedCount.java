package com.okwei.detail.bean.vo;

public class RelatedCount {
    /**
     * 产品数量
     */
    public long proCount;
    /**
     * 用户数量
     */
    public long userCount;
    /**
     * 批发商数量
     */
    public long pfsCount;
    public long getProCount() {
        return proCount;
    }
    public void setProCount(long proCount) {
        this.proCount = proCount;
    }
    public long getUserCount() {
        return userCount;
    }
    public void setUserCount(long userCount) {
        this.userCount = userCount;
    }
    public long getPfsCount() {
        return pfsCount;
    }
    public void setPfsCount(long pfsCount) {
        this.pfsCount = pfsCount;
    }
    
}
