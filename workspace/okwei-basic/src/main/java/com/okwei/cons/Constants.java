package com.okwei.cons;

import java.io.Serializable;

/**
 * 
 * @ClassName: Constants
 * @Description: 常量类
 * @author xiehz
 * @date 2015年5月18日 下午2:22:04
 *
 */
public class Constants {

	/**
	 * 
	 * @Title: getValidateKey
	 * @Description: TODO(这里用一句话描述这个方法的作用)
	 * @param @param id
	 * @param @return
	 * @return String
	 * @throws
	 */
	public final static String getValidateKey(String id) {
		return "S_" + id + "_ICODE";
	}

	// 用户session key
	public final static String getUserSessionKey(Serializable userId) {
		return "USER_SESSION_KEY_" + userId;
	}

	// 用户loginInfo id key
	public final static String getLoginInfoIdCacheKey(Serializable userId) {
		return "LOGIN_INFO_ID_KEY_" + userId;
	}

}
