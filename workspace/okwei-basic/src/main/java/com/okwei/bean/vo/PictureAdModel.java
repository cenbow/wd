package com.okwei.bean.vo;

import java.io.Serializable;

public class PictureAdModel implements Serializable{
	/**
	 * BA01 品牌分销首页广告
	 */
	private static final long serialVersionUID = 1L;
	private Integer adId;//广告ID
	private int positionType; //广告位类型，0为购物首页广告，1为分享首页广告
	private String title;//广告标题
	private String url;//广告链接wap页地址，可用于二维码扫描
	private String imageUrl;//广告图片URL
	private String publishTime;//发布时间
	/**
	 * @return the adId
	 */
	public Integer getAdId() {
		return adId;
	}
	/**
	 * @param adId the adId to set
	 */
	public void setAdId(Integer adId) {
		this.adId = adId;
	}
	/**
	 * @return the positionType
	 */
	public int getPositionType() {
		return positionType;
	}
	/**
	 * @param positionType the positionType to set
	 */
	public void setPositionType(int positionType) {
		this.positionType = positionType;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	/**
	 * @return the imageUrl
	 */
	public String getImageUrl() {
		return imageUrl;
	}
	/**
	 * @param imageUrl the imageUrl to set
	 */
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	/**
	 * @return the publishTime
	 */
	public String getPublishTime() {
		return publishTime;
	}
	/**
	 * @param publishTime the publishTime to set
	 */
	public void setPublishTime(String publishTime) {
		this.publishTime = publishTime;
	}
	
}
