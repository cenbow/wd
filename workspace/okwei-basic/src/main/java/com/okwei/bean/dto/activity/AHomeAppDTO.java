package com.okwei.bean.dto.activity;

import java.io.Serializable;

import com.okwei.bean.domain.AHomeApp;


public class AHomeAppDTO implements Serializable{

	private static final long serialVersionUID = 1L;
	
	private AHomeApp homeapp;
	private int classId;
	private String classNames;

	public int getClassId() {
		return classId;
	}
	public void setClassId(int classId) {
		this.classId = classId;
	}
	public String getClassNames() {
		return classNames;
	}
	public void setClassNames(String className) {
		this.classNames = className;
	}
	public AHomeApp getHomeapp() {
		return homeapp;
	}
	public void setHomeapp(AHomeApp homeapp) {
		this.homeapp = homeapp;
	}
		
}
