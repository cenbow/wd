package com.okwei.bean.enums;

public enum OrderFromTypeEnum {
	/**
     * 正常
     */
    Normal(1),
    /**
     * 分享页过来的
     */
    Share(2),
    /**
     * 其它
     */
    Other(3);
    private final int Type;

    private OrderFromTypeEnum(int step)
    {
        this.Type = step;
    }

    public String toString()
    {
        return String.valueOf(this.Type);
    }

}
