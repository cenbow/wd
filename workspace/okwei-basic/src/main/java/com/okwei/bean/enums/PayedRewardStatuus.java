package com.okwei.bean.enums;

/**
 * 悬赏金额的支付状态
 * 
 * @author Administrator
 *
 */
public enum PayedRewardStatuus
{
    /**
     * 未支付
     */
    No(0),
    /**
     * 已支付
     */
    Yes(1);
    private final int Type;

    private PayedRewardStatuus(int step)
    {
        this.Type = step;
    }

    public String toString()
    {
        return String.valueOf(this.Type);
    }
}
