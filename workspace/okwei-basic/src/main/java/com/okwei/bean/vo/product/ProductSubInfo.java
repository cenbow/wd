package com.okwei.bean.vo.product;

import java.util.List;
import java.util.Map;

public class ProductSubInfo {
	private String productTitle;
	private String productTitleMin;
	private String description;
	private String defaultImg;
	private List<Map<String,Object>> proImgs;
	private Map<String,Double> price;
	private List<SellAtrrEdit> sellAttr;
	private Long productId;
	
	private Integer brandID;
	
	
	
	public Integer getBrandID() {
		return brandID;
	}
	public void setBrandID(Integer brandID) {
		this.brandID = brandID;
	}
	public String getProductTitle() {
		return productTitle;
	}
	public void setProductTitle(String productTitle) {
		this.productTitle = productTitle;
	}
	public String getProductTitleMin() {
		return productTitleMin;
	}
	public void setProductTitleMin(String productTitleMin) {
		this.productTitleMin = productTitleMin;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDefaultImg() {
		return defaultImg;
	}
	public void setDefaultImg(String defaultImg) {
		this.defaultImg = defaultImg;
	}
	public List<Map<String, Object>> getProImgs() {
		return proImgs;
	}
	public void setProImgs(List<Map<String, Object>> proImgs) {
		this.proImgs = proImgs;
	}
	public Map<String, Double> getPrice() {
		return price;
	}
	public void setPrice(Map<String, Double> price) {
		this.price = price;
	}
	public List<SellAtrrEdit> getSellAttr() {
		return sellAttr;
	}
	public void setSellAttr(List<SellAtrrEdit> sellAttr) {
		this.sellAttr = sellAttr;
	}
	public Long getProductId() {
		return productId;
	}
	public void setProductId(Long productId) {
		this.productId = productId;
	}
	
	

}
