package com.okwei.bean.vo.user;

public class ChannelInfoVO {
	
	private Long weiId;
	
	private String weiName;
	
	private Short channelType;
	
	private String channelName;
	
	private String linkMan;
	
	private String phone;
	
	private String address;
	
	private String image;
	

	public String getWeiName() {
		return weiName;
	}

	public void setWeiName(String weiName) {
		this.weiName = weiName;
	}

	public String getLinkMan() {
		return linkMan;
	}

	public void setLinkMan(String linkMan) {
		this.linkMan = linkMan;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public Long getWeiId() {
		return weiId;
	}

	public void setWeiId(Long weiId) {
		this.weiId = weiId;
	}

	public Short getChannelType() {
		return channelType;
	}

	public void setChannelType(Short channelType) {
		this.channelType = channelType;
	}

	public String getChannelName() {
		return channelName;
	}

	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	
}
