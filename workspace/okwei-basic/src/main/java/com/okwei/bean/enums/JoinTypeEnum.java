package com.okwei.bean.enums;
/**
 * 申请的来源
 * @author Administrator
 *
 */
public enum JoinTypeEnum
{
    /**
     * 自己申请的(系统申请)
     */
    oneself(0),
    /**
     * 供应商添加
     */
    supplier(1);

    private final int step; 

private JoinTypeEnum(int step) { 
     this.step = step; 
}
@Override
public String toString()
{
    return String.valueOf(this.step);               
}
}
