package com.okwei.bean.enums;

/**
 * 代理商申请状态 　
 */
public enum ApplyAgentStatusEnum
{
    /**
     * 申请中
     */
    Applying(0),
    /**
     * 审核通过
     */
    Pass(1),
    /**
     * 审核不通过
     */
    NoPass(2),
    /**
     * 删除
     */
    Delete(3);
    private final int Type;

    private ApplyAgentStatusEnum(int step)
    {
        this.Type = step;
    }

    public String toString()
    {
        return String.valueOf(this.Type);
    }
}
