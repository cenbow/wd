package com.okwei.bean.enums;

public enum FriendScope {

	/**
	 * 分销商可见
	 */
	child(1),
	/**
	 * 粉丝可见
	 */
	fans(2),
	/**
	 * 朋友可见
	 */
	friends(4);

	private final int step; 

    private FriendScope(int step) { 

         this.step = step; 
    }
    @Override
    public String toString()
    {
    	return String.valueOf(this.step);         	
    }
}
