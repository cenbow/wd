package com.okwei.bean.enums.agent;
/**
 * 供应商类型
 * @author Administrator
 *
 */
public enum AgentSupplierType {
	/**
     * 联合分销号
     */
	union(1),
	/**
     * 独立分销号
     */
    independence(2);
    private final int Type;

    private AgentSupplierType(int step)
    {
        this.Type = step;
    }

    public String toString()
    {
        return String.valueOf(this.Type);
    }
    
    public int toInt()
    {
    	return this.Type;
    }
}
