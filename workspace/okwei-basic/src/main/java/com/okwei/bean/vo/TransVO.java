package com.okwei.bean.vo;

import java.io.Serializable;

/**
 * 封装物流信息
 * 
 * @author 欧阳
 *
 */
public class TransVO implements Serializable {

	/**
	 * 物流公司代号
	 */
	private String transCompanyNo;
	/**
	 * 物流公司名
	 */
	private String transCompany;
	/**
	 * 物流单号
	 */
	private String transNo;
	/**
	 * 物流详情
	 */
	private String wuliu;
	/**
	 * 订单号
	 */
	private String orderNo;
	
	/**
	 * 卖家
	 */
	private Long sellerWeiid;
	/**
	 * 买家
	 */
	private Long buyerWeiid;
	/**
	 * 订单状态
	 */
	private Integer orderState;

	public String getTransCompany() {
		return transCompany;
	}

	public void setTransCompany(String transCompany) {
		this.transCompany = transCompany;
	}

	public String getTransNo() {
		return transNo;
	}

	public void setTransNo(String transNo) {
		this.transNo = transNo;
	}

	public String getWuliu() {
		return wuliu;
	}

	public void setWuliu(String wuliu) {
		this.wuliu = wuliu;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getTransCompanyNo() {
		return transCompanyNo;
	}

	public void setTransCompanyNo(String transCompanyNo) {
		this.transCompanyNo = transCompanyNo;
	}

	public Long getSellerWeiid() {
		return sellerWeiid;
	}

	public void setSellerWeiid(Long sellerWeiid) {
		this.sellerWeiid = sellerWeiid;
	}

	public Long getBuyerWeiid() {
		return buyerWeiid;
	}

	public void setBuyerWeiid(Long buyerWeiid) {
		this.buyerWeiid = buyerWeiid;
	}

	public Integer getOrderState() {
		return orderState;
	}

	public void setOrderState(Integer orderState) {
		this.orderState = orderState;
	}

	
}
