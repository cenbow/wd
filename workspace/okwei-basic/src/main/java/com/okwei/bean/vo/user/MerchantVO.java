package com.okwei.bean.vo.user;

import java.io.Serializable;
import java.util.Date;

/**
 * 供应商
 */
public class MerchantVO implements Serializable
{
    private static final long serialVersionUID = -8136734386620317891L;
    /**
     * 供应商微店号
     */
    private Long merchantWeiId;
    /**
     * 供应商名
     */
    private String merchantName;
    /**
     * 供应商店铺图片
     */
    private String shopImg;
    /**
     * 联系人
     */
    private String linkName;
    /**
     * 联系电话
     */
    private String phone;
    /**
     * 公司详情
     */
    private String details;
    /**
     * 保证金
     */
    private Double bond;
    /**
     * 店鋪進駐時間
     */
    private Date shopTime;
    /**
     * 客服qq
     */
    private String qq;
    /**
     * 区域地址
     */
    private String areaAddres;
    /**
     * 是否关注
     */
    private Integer attention;

    public Integer getAttention()
    {
        return attention;
    }

    public void setAttention(Integer attention)
    {
        this.attention = attention;
    }

    public String getAreaAddres()
    {
        return areaAddres;
    }

    public void setAreaAddres(String areaAddres)
    {
        this.areaAddres = areaAddres;
    }

    public Date getShopTime()
    {
        return shopTime;
    }

    public void setShopTime(Date shopTime)
    {
        this.shopTime = shopTime;
    }

    public String getQq()
    {
        return qq;
    }

    public void setQq(String qq)
    {
        this.qq = qq;
    }

    public Double getBond()
    {
        return bond;
    }

    public void setBond(Double bond)
    {
        this.bond = bond;
    }

    public String getDetails()
    {
        return details;
    }

    public void setDetails(String details)
    {
        this.details = details;
    }

    public Long getMerchantWeiId()
    {
        return merchantWeiId;
    }

    public void setMerchantWeiId(Long merchantWeiId)
    {
        this.merchantWeiId = merchantWeiId;
    }

    public String getMerchantName()
    {
        return merchantName;
    }

    public void setMerchantName(String merchantName)
    {
        this.merchantName = merchantName;
    }

    public String getShopImg()
    {
        return shopImg;
    }

    public void setShopImg(String shopImg)
    {
        this.shopImg = shopImg;
    }

    public String getLinkName()
    {
        return linkName;
    }

    public void setLinkName(String linkName)
    {
        this.linkName = linkName;
    }

    public String getPhone()
    {
        return phone;
    }

    public void setPhone(String phone)
    {
        this.phone = phone;
    }
}
