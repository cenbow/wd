package com.okwei.bean.enums;

/**
 * (代理商/落地店)状态
 */
public enum AgentStatusEnum
{
    /**
     * 正常
     */
    Normal(1),
    /**
     * 取消
     */
    Cancel(2),
    /**
     * 删除代理商/落地店
     */
    Delete(3);
    private final int Type;

    private AgentStatusEnum(int step)
    {
        this.Type = step;
    }

    public String toString()
    {
        return String.valueOf(this.Type);
    }
}
