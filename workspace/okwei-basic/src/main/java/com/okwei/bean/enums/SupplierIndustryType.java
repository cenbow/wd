package com.okwei.bean.enums;
public enum SupplierIndustryType {
	/**
	 * 云商通
	 */
    Yun(1),
    /**
     * 批发号供应商
     */
    Bacht(2),
    /**
     * 平台号
     */
    platform(3),
    /**
     * 品牌号
     */
    brand(4);
	private final int step; 
    private SupplierIndustryType(int step) { 

         this.step = step; 
    }
    @Override
    public String toString()
    {
    	return String.valueOf(this.step);         	
    }
}