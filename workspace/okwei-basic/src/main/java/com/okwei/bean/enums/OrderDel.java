package com.okwei.bean.enums;

public enum OrderDel
{
	/**
	 * 删除
	 */
	Del(2),
	/**
	 * 未删除
	 */
	NoDel(1);
	private final int step; 

    private OrderDel(int step)
    { 

         this.step = step; 
    }
    
    @Override
    public String toString()
    {
    	return String.valueOf(this.step);         	
    }
}
