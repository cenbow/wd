package com.okwei.bean.enums;

public enum FriendReplyEnum {
	/**
	 * 申请中
	 */
	Applying(0),
	/**
	 * 同意
	 */
	Agree(1),
	/**
	 * 拒绝
	 */
	Refuse(2);


	private final int step;

	private FriendReplyEnum(int step) {

		this.step = step;
	}

	@Override
	public String toString() {
		return String.valueOf(this.step);
	}
}
