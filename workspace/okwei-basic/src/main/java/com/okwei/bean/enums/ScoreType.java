package com.okwei.bean.enums;

/**
 * 产品得分 类型
 * @author Administrator
 *
 */
public enum ScoreType {
	/**
	 * 成交
	 */
	Order(1),
	/**
	 *  评论
	 */
	Pinglun(2),
	/**
	 * 点赞
	 */
	Dianzan(3),
	/**
	 * 上架
	 */
	Onshelve(4),
	/**
	 * 收藏
	 */
	Collect(5),
	/**
	 * 阅读
	 */
	read(6),
	/**
	 * 分享
	 */
	Share(7);

	private final int step;

	private ScoreType(int step) {
		this.step = step;
	}

	@Override
	public String toString() {
		return String.valueOf(this.step);
	}
}
