package com.okwei.bean.enums;

public enum DemandStateEnum {

	/**
	 * 草稿
	 */
	Draft(0),
	/**
	 * 展示中
	 */
	Showing(1),
	/**
	 * 下架
	 */
	OffShelf(2),
	/**
	 * 删除
	 */
	delete(3),
	/**
	 * 待审核
	 */
	WaitAuditing(4),
	/**
	 * 不通过
	 */
	NoPass(5);
		
	private final int Type;

	private DemandStateEnum(int step) {
		this.Type = step;
	}

	public String toString() {
		return String.valueOf(this.Type);
	}
}
