package com.okwei.bean.enums;

//模块ID
public enum ModelType {
	 /// 用户
		user(0),
		//产品
		product(1),
		//订单
		order(2);
		private final int step; 

	    private ModelType(int step)
	    { 
	         this.step = step; 
	    }
	    @Override
	    public String toString()
	    {
	    	return String.valueOf(this.step);         	
	    }

}
