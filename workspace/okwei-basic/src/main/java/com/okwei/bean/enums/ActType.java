package com.okwei.bean.enums;

public enum ActType {
	/**
     * 限时抢购
     */
    time(0),
	/**
     * 全返
     */
    quanFan(1);
    private final int Type;

    private ActType(int step)
    {
        this.Type = step;
    }

    public String toString()
    {
        return String.valueOf(this.Type);
    }
}
