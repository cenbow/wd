package com.okwei.dao.impl;

import org.springframework.orm.hibernate4.HibernateTemplate;


public class GetHibernateTemplate {

	private HibernateTemplate hibernateTemplate;
	
	private HibernateTemplate readHibernateTemplate;
		
	
	public HibernateTemplate getHibernateTemplate() {
		return hibernateTemplate;
	}

	public void setHibernateTemplate(HibernateTemplate hibernateTemplate) {
		this.hibernateTemplate = hibernateTemplate;
	}
	
	public HibernateTemplate getReadHibernateTemplate() {
		return readHibernateTemplate;
	}
	public void setReadHibernateTemplate(HibernateTemplate readHibernateTemplate) {
		this.readHibernateTemplate = readHibernateTemplate;
	}
}
